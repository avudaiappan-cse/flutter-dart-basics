void main() {
//Declaring variables in Dart

 //Using var type cannot be change statically typed
  
  var myName = 'Avinash'; // Assigned value of any type

//Declaring Strings in Dart

  String name = 'Avinash';

//Declaring int in Dart

  int age = 20;

//Declaring double in Dart

  double num = 1332525434.787;

//For assigning dynamic type

  dynamic imAcceptAnyType = 'ThisisString';

  print(myName);
  print(name);
  print(age);
  print(num);
  print(imAcceptAnyType);
}
